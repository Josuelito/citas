     void listar() {
        txtNumero.requestFocus();
        String sql = "Select citPaciente,citMedico,citConsultorio,citFecha,citHora,citNumero from citas";
        try {
            con = cn.getConnection(); 
            st = con.createStatement();
            rs = st.executeQuery(sql); 
            Object[] persona = new Object[6];
            model = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                persona[0] = rs.getString("citPaciente");
                persona[1] = rs.getString("citMedico");
                persona[2] = rs.getInt("citConsultorio");
                persona[3] = rs.getString("citFecha");
                persona[4] = rs.getString("citHora");
                persona[5] = rs.getInt("citNumero");
                model.addRow(persona);
            }
            TablaDatos.setModel(model);
        } catch (Exception e) {
        }
    }

    public void agregar() {
        String IdP = TxtIdPaciente.getText();
        String IdM = txtIdMedico.getText();
        String Con = txtConsultorio.getText();
        Date Fec = fechacita.getDate();
        String Hor = txtHora.getText();
        String Num = txtNumero.getText();
         try {
            if (IdP.equals("") || IdM.equals("") || Con.equals("") || Fec.equals("") || Hor.equals("") || Num.equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar datos");
                limpiarTabla(model);
                } else {
                String sql = "insert into citas(citNumero, citFecha, citHora, citPaciente, citMedico, citConsultorio)"
                        + "values ('" + Num + "','" + Fec + "','" + Hor + "','" + IdP + "','" + IdM + "','" + Con + "')";
                con=cn.getConnection();
                st=con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario registrado con exito");
                limpiarTabla(model);
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error"+ e.getMessage());
    }
         
}
    void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i <= TablaDatos.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }
