     void modificar() {
        String IdP = TxtIdPaciente.getText();
        String IdM = txtIdMedico.getText();
        String Con = txtConsultorio.getText();
        String Fec = fechacita.getDateFormatString();
        String Hor = txtHora.getText();
        String Num = txtNumero.getText();

        String sql = "'" + IdP + "',citPaciente='" + IdM + "',citMedico='"
                + Con + "',citConsultorio='" + Fec + "',citFecha='"
                + Hor + "',citHora='" + Num + "'citNumero'";
        try {
                if (IdP != null || IdM != null || Con != null ||  Fec!= null || Hor!= null || Num!= null){
                    con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario modificado con exito");
                limpiarTabla(model);
                } else {
                JOptionPane.showMessageDialog(null, "Error!.....");
                limpiarTabla(model);
                }
        } catch (Exception e) {
        }
       
    }
     void eliminar() {
        String sql = "delete citPaciente,citMedico,citConsultorio,citFecha,citHora,citNumero, citEstado from citas=" + id;
        int fila = TablaDatos.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Usuario no seleccionado");
        } else {
            try {
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "usuario eliminado");
                limpiarTabla(model);

            } catch (Exception e) {
            }
        }
    }
        
